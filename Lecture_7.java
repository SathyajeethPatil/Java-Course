package Lecture;

public class Lecture_7 {

	public static void main(String[] args) {
		// If Loops
		
		int id = 10;
		if(id<10) {
			System.out.printf("%d is Lesser Than 10", id);
		}
		else if(id>10){
			System.out.printf("%d Greater Than 10", id);
		}
		else {
			System.out.printf("%d is Equal to 10", id);
		}
	}

}
